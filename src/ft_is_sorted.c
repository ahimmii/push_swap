#include "../ft_push_swap.h"

char	*ft_is_sorted(int *tab, int ac)
{
	int	i;

	i = 0;
	while (i < ac - 2)
	{
		if (tab[i] < tab[i + 1])
		{
			i++;
		}
		else
			return ("KO\n");
	}
	return ("OK\n");
}
