#include "../ft_push_swap.h"

int	ft_size_tab(int ac, char *av[])
{
	int	i;
	int	j;
	int	x;

	i = 1;
	j = 0;
	x = 0;
	while (i < ac)
	{
		j = 0;
		if (av[i][j] != ' ')
			x++;
		while (av[i][j])
		{
			if (av[i][j] == ' ' && av[i][j + 1] >= 48 && av[i][j + 1] <= 57)
				x++;
			j++;
		}
		i++;
	}
	return (x);
}
